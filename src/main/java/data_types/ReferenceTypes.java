package data_types;

public class ReferenceTypes {
    /**
     * Reference Data Types:
     * 1. Class types
     * 2. Interface types
     * 3. Enums
     * 4. Array types
     * 5. Records
     * 6. Annotations
     **/
    public int value;

    public ReferenceTypes(int value) {
        this.value = value;
    }

    public int getValue() {
        return value;
    }

    public void setValue(int value) {
        this.value = value;
    }
}
